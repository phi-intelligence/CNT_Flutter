import 'package:flutter/material.dart';
import '../../theme/app_colors.dart';
import '../../theme/app_spacing.dart';
import 'audio_preview_screen.dart';

/// Audio Recording Screen - Record audio podcasts
class AudioRecordingScreen extends StatefulWidget {
  const AudioRecordingScreen({super.key});

  @override
  State<AudioRecordingScreen> createState() => _AudioRecordingScreenState();
}

class _AudioRecordingScreenState extends State<AudioRecordingScreen> {
  bool _isRecording = false;
  bool _isPaused = false;
  int _recordingDuration = 0;
  int _pausedDuration = 0;

  @override
  void initState() {
    super.initState();
  }

  void _startRecording() {
    setState(() {
      _isRecording = true;
      _isPaused = false;
    });
    _updateDuration();
  }

  void _pauseRecording() {
    setState(() {
      _isPaused = true;
    });
  }

  void _resumeRecording() {
    setState(() {
      _isPaused = false;
    });
    _updateDuration();
  }

  void _stopAndSave() {
    setState(() {
      _isRecording = false;
    });
    // Navigate to audio preview screen
    Navigator.pushReplacement(
      context,
      MaterialPageRoute(
        builder: (context) => AudioPreviewScreen(
          audioUri: 'recording_path',
          source: 'recording',
          duration: _recordingDuration,
          fileSize: _recordingDuration * 1024 * 16, // Estimate 16KB per second
        ),
      ),
    );
  }

  void _discardRecording() {
    setState(() {
      _isRecording = false;
      _recordingDuration = 0;
      _pausedDuration = 0;
    });
    Navigator.pop(context);
  }

  void _updateDuration() {
    if (_isRecording && !_isPaused) {
      Future.delayed(const Duration(seconds: 1), () {
        if (mounted && _isRecording && !_isPaused) {
          setState(() {
            _recordingDuration++;
          });
          _updateDuration();
        }
      });
    }
  }

  String _formatDuration(int seconds) {
    final mins = seconds ~/ 60;
    final secs = seconds % 60;
    return '${mins.toString().padLeft(1, '0')}:${secs.toString().padLeft(2, '0')}';
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: AppColors.backgroundPrimary,
      appBar: AppBar(
        backgroundColor: AppColors.backgroundPrimary,
        elevation: 0,
        leading: IconButton(
          icon: const Icon(Icons.close),
          onPressed: () => Navigator.pop(context),
        ),
        title: const Text('Record Audio'),
      ),
      body: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          // Timer display
          Text(
            _formatDuration(_recordingDuration),
            style: const TextStyle(fontSize: 48, fontWeight: FontWeight.bold),
          ),
          const SizedBox(height: AppSpacing.medium),

          // Recording indicator
          if (_isRecording)
            Container(
              width: 16,
              height: 16,
              decoration: const BoxDecoration(
                shape: BoxShape.circle,
                color: Colors.red,
              ),
            ),
          const SizedBox(height: AppSpacing.extraLarge),

          // Waveform visualization (placeholder)
          Container(
            width: double.infinity,
            height: 100,
            margin: EdgeInsets.symmetric(horizontal: AppSpacing.large),
            decoration: BoxDecoration(
              color: AppColors.backgroundSecondary,
              borderRadius: BorderRadius.circular(AppSpacing.radiusMedium),
            ),
            child: Center(
              child: Icon(
                Icons.graphic_eq,
                size: 48,
                color: AppColors.textSecondary,
              ),
            ),
          ),

          const SizedBox(height: AppSpacing.extraLarge),

          // Control buttons
          Row(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              if (!_isRecording)
                IconButton(
                  iconSize: 64,
                  icon: Container(
                    width: 64,
                    height: 64,
                    decoration: BoxDecoration(
                      shape: BoxShape.circle,
                      color: AppColors.successMain,
                    ),
                    child: const Icon(Icons.mic, color: Colors.white),
                  ),
                  onPressed: _startRecording,
                )
              else ...[
                if (_isPaused)
                  IconButton(
                    iconSize: 48,
                    icon: const Icon(Icons.play_arrow, color: AppColors.primaryMain),
                    onPressed: _resumeRecording,
                  )
                else
                  IconButton(
                    iconSize: 48,
                    icon: const Icon(Icons.pause, color: AppColors.primaryMain),
                    onPressed: _pauseRecording,
                  ),
                const SizedBox(width: AppSpacing.medium),
                IconButton(
                  iconSize: 64,
                  icon: Container(
                    width: 64,
                    height: 64,
                    decoration: const BoxDecoration(
                      shape: BoxShape.circle,
                      color: AppColors.successMain,
                    ),
                    child: const Icon(Icons.check, color: Colors.white),
                  ),
                  onPressed: _stopAndSave,
                ),
                const SizedBox(width: AppSpacing.medium),
                IconButton(
                  iconSize: 48,
                  icon: const Icon(Icons.delete, color: AppColors.errorMain),
                  onPressed: _discardRecording,
                ),
              ],
            ],
          ),
        ],
      ),
    );
  }
}

